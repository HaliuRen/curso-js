const dinero = 300;
const totalAPagar= 300;

// mayor que

if( dinero > totalAPagar ) {
    console.log('Si podemos pagar');
}else {
    console.log('Fondos insuficientes');
}

// menor que

if( dinero < totalAPagar ) {
    console.log('Fondos insuficientes');
} else {
    console.log('Si podemos pagar');
}

